class Increment {
	public static void main(String args[]) {
		int a=20;
		System.out.println("the value of a is:"+a);
		System.out.println("the value of a is after post increment of a is"+(--a));
		System.out.println();
		int b=30;
		System.out.println("the value of b is"+b);
		System.out.println("the value of b is after pre increment of b is "+(b--));
		System.out.println("the value of b is"+b);
	}
}
